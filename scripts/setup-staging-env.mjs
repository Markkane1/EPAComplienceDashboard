import { randomBytes } from "crypto";
import { chmod, writeFile } from "fs/promises";
import path from "path";
import { fileURLToPath } from "url";
import { createInterface } from "readline/promises";
import process from "process";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, "..");
const apiEnvFile = path.join(rootDir, "apps", "api", ".env");
const webEnvFile = path.join(rootDir, "apps", "web", ".env.staging");

const rl = createInterface({ input: process.stdin, output: process.stdout });

const ask = async (question, defaultValue = "") => {
  const suffix = defaultValue ? ` [${defaultValue}]` : "";
  const answer = (await rl.question(`${question}${suffix}: `)).trim();
  return answer || defaultValue;
};

const askRequired = async (question, defaultValue = "") => {
  while (true) {
    const value = await ask(question, defaultValue);
    if (value) return value;
    console.log("This value is required.");
  }
};

const main = async () => {
  console.log("==============================================");
  console.log("EPA Compliance Dashboard - Staging Setup");
  console.log("==============================================");
  console.log("");

  const generatedJwt = randomBytes(32).toString("hex");

  const stagingWebDomain = await askRequired("Staging web domain", "staging.example.com");
  const stagingApiDomain = await askRequired("Staging API domain", "staging-api.example.com");
  const mongoUri = await askRequired("MongoDB URI");
  const jwtSecret = await askRequired("JWT secret", generatedJwt);
  const adminPassword = await askRequired("Admin password");
  const adminEmail = await ask(
    "Admin email",
    `admin-staging@${stagingWebDomain}`
  );
  const adminName = await ask("Admin full name", "Staging Administrator");
  const adminCnic = await ask("Admin CNIC", "staging-admin-cnic");
  const resendApiKey = await askRequired("Resend API key (re_...)");
  const emailFrom = await ask("Resend sender email", "onboarding@resend.dev");
  const emailFromName = await ask(
    "Resend sender name",
    "EPA Compliance Dashboard (Staging)"
  );

  const apiEnv = [
    "# Auto-generated by scripts/setup-staging-env.mjs",
    "NODE_ENV=production",
    "PORT=4000",
    `MONGO_URI=${mongoUri}`,
    `JWT_SECRET=${jwtSecret}`,
    `ADMIN_PASSWORD=${adminPassword}`,
    `ADMIN_EMAIL=${adminEmail}`,
    `ADMIN_NAME=${adminName}`,
    `ADMIN_CNIC=${adminCnic}`,
    `CORS_ORIGIN=https://${stagingWebDomain}`,
    `PUBLIC_BASE_URL=https://${stagingApiDomain}`,
    `APP_BASE_URL=https://${stagingWebDomain}`,
    "UPLOAD_DIR=./uploads",
    "SECURE_UPLOADS_DIR=../secure-uploads",
    `RESEND_API_KEY=${resendApiKey}`,
    `EMAIL_FROM=${emailFrom}`,
    `EMAIL_FROM_NAME=${emailFromName}`,
    "",
  ].join("\n");

  const webEnv = [
    "# Auto-generated by scripts/setup-staging-env.mjs",
    `VITE_API_URL=https://${stagingApiDomain}`,
    "",
  ].join("\n");

  await writeFile(apiEnvFile, apiEnv, "utf8");
  await writeFile(webEnvFile, webEnv, "utf8");

  try {
    await chmod(apiEnvFile, 0o600);
  } catch {
    // Ignore chmod failures on platforms that do not fully support POSIX permissions.
  }

  console.log("");
  console.log("Created:");
  console.log(`  - ${apiEnvFile}`);
  console.log(`  - ${webEnvFile}`);
  console.log("");
  console.log("Next steps:");
  console.log("  1) npm run build:staging");
  console.log("  2) npm run start:staging:api");
};

main()
  .catch((error) => {
    console.error("Failed to configure staging environment:", error);
    process.exitCode = 1;
  })
  .finally(() => rl.close());
