import { randomBytes } from "crypto";
import { chmod, writeFile } from "fs/promises";
import path from "path";
import { fileURLToPath } from "url";
import { createInterface } from "readline/promises";
import process from "process";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const rootDir = path.resolve(__dirname, "..");
const apiEnvFile = path.join(rootDir, "apps", "api", ".env");
const webEnvFile = path.join(rootDir, "apps", "web", ".env.production");

const rl = createInterface({ input: process.stdin, output: process.stdout });

const ask = async (question, defaultValue = "") => {
  const suffix = defaultValue ? ` [${defaultValue}]` : "";
  const answer = (await rl.question(`${question}${suffix}: `)).trim();
  return answer || defaultValue;
};

const askRequired = async (question, defaultValue = "") => {
  while (true) {
    const value = await ask(question, defaultValue);
    if (value) return value;
    console.log("This value is required.");
  }
};

const quoteEnvValue = (value) => {
  return `"${String(value).replace(/\\/g, "\\\\").replace(/"/g, '\\"')}"`;
};

const toEnvLine = (key, value) => `${key}=${quoteEnvValue(value)}`;
const isIpHost = (host) => /^\d{1,3}(\.\d{1,3}){3}$/.test(host);

const main = async () => {
  console.log("==============================================");
  console.log("EPA Compliance Dashboard - Production Setup");
  console.log("==============================================");
  console.log("");

  const generatedJwt = randomBytes(32).toString("hex");

  const hostOrIp = await askRequired("Public web host or VM IP", "YOUR_VM_PUBLIC_IP");
  const normalizedWebHost = hostOrIp.replace(/^https?:\/\//, "").replace(/\/+$/, "");
  const inferredApiHost = isIpHost(normalizedWebHost)
    ? normalizedWebHost
    : `api.${normalizedWebHost.replace(/^www\./, "").replace(/^app\./, "")}`;
  const apiHostOrIp = await ask("Public API host or VM IP", inferredApiHost);
  const normalizedApiHost = (apiHostOrIp || normalizedWebHost).replace(/^https?:\/\//, "").replace(/\/+$/, "");
  const protocol = await ask("Protocol (http or https)", "http");
  const mongoUri = await askRequired("MongoDB URI");
  const jwtSecret = await askRequired("JWT secret", generatedJwt);
  const adminPassword = await askRequired("Admin password");
  const adminEmail = await ask("Admin email", "admin@local.test");
  const adminName = await ask("Admin full name", "System Administrator");
  const adminCnic = await ask("Admin CNIC", "admin-cnic");
  const resendApiKey = await ask("Resend API key (re_...)");
  const emailFrom = await ask("Resend sender email", "onboarding@resend.dev");
  const emailFromName = await ask("Resend sender name", "EPA Compliance Dashboard");
  const extraCorsOrigins = await ask("Extra CORS origins (comma-separated, optional)");

  const scheme = protocol.toLowerCase() === "https" ? "https" : "http";
  const appBaseUrl = `${scheme}://${normalizedWebHost}`;
  const publicBaseUrl = `${scheme}://${normalizedApiHost}`;
  const viteApiUrl = publicBaseUrl;

  const corsOrigins = new Set([appBaseUrl]);
  if (!isIpHost(normalizedWebHost) && !normalizedWebHost.startsWith("www.")) {
    corsOrigins.add(`${scheme}://www.${normalizedWebHost}`);
  }
  if (extraCorsOrigins) {
    for (const rawOrigin of extraCorsOrigins.split(",")) {
      const origin = rawOrigin.trim();
      if (origin) corsOrigins.add(origin);
    }
  }

  const apiEnv = [
    "# Auto-generated by scripts/setup-production-env.mjs",
    "# Production API environment",
    toEnvLine("NODE_ENV", "production"),
    toEnvLine("PORT", "8080"),
    toEnvLine("ENFORCE_HTTPS", scheme === "https" ? "true" : "false"),
    toEnvLine("MONGO_URI", mongoUri),
    toEnvLine("JWT_SECRET", jwtSecret),
    toEnvLine("ADMIN_PASSWORD", adminPassword),
    toEnvLine("ADMIN_EMAIL", adminEmail),
    toEnvLine("ADMIN_NAME", adminName),
    toEnvLine("ADMIN_CNIC", adminCnic),
    toEnvLine("CORS_ORIGIN", [...corsOrigins].join(",")),
    toEnvLine("PUBLIC_BASE_URL", publicBaseUrl),
    toEnvLine("APP_BASE_URL", appBaseUrl),
    toEnvLine("UPLOAD_DIR", "./uploads"),
    toEnvLine("SECURE_UPLOADS_DIR", "../secure-uploads"),
    toEnvLine("RESEND_API_KEY", resendApiKey),
    toEnvLine("EMAIL_FROM", emailFrom),
    toEnvLine("EMAIL_FROM_NAME", emailFromName),
    "",
  ].join("\n");

  const webEnv = [
    "# Auto-generated by scripts/setup-production-env.mjs",
    "# Production web environment",
    toEnvLine("VITE_API_URL", viteApiUrl),
    "",
  ].join("\n");

  await writeFile(apiEnvFile, apiEnv, "utf8");
  await writeFile(webEnvFile, webEnv, "utf8");

  try {
    await chmod(apiEnvFile, 0o600);
  } catch {
    // Ignore chmod failures on filesystems without POSIX permissions.
  }

  console.log("");
  console.log("Created:");
  console.log(`  - ${apiEnvFile}`);
  console.log(`  - ${webEnvFile}`);
  console.log("");
  console.log("Next steps:");
  console.log("  1) npm run build:production");
  console.log("  2) npm run deploy:oracle");
};

main()
  .catch((error) => {
    console.error("Failed to configure production environment:", error);
    process.exitCode = 1;
  })
  .finally(() => rl.close());
